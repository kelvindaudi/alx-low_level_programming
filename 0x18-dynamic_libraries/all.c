#include <stdio.h>

/** 
 * add: add 2 integers
 * @a: first int
 * @b: 2nd int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/** 
 * sub: subtract 2 integers
 * @a: first int
 * @b: 2nd int
 * Return: substracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}
/** 
 * mod: mod 2 integers
 * @a: first int
 * @b: 2nd int
 * Return: final result
 */
int mod(int a, int b)
{
	return (a % b);
}
/** 
 * mul - multiply 2 integers
 * @a: first int
 * @b: 2nd int
 * Return: final result
 */
int mul(int a, int b)
{
	return (a * b);
}
/** 
 * div - divide 2 integers
 * @a: first int
 * @b: 2nd int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
